const chatbotToggle = document.getElementById("chatbot-toggle");
const chatbotClose = document.getElementById("chatbot-close");
const chatbotPanel = document.getElementById("chatbot-panel");
const chatbotMessages = document.getElementById("chatbot-messages");
const chatbotForm = document.getElementById("chatbot-form");
const chatbotInput = document.getElementById("chatbot-input");
const quickQuestionButtons = document.querySelectorAll("[data-chat-topic]");
const bottomMessage = document.getElementById("bottom-message");
let chatbotStarted = false;

function getCurrentLanguage() {
  return document.documentElement.lang === "en" ? "en" : "es";
}

function addChatMessage(message, sender) {
  const bubble = document.createElement("div");
  bubble.className = `chatbot-message ${sender}`;
  bubble.textContent = message;
  chatbotMessages.appendChild(bubble);
  chatbotMessages.scrollTop = chatbotMessages.scrollHeight;
}

function normalizeText(text) {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function detectTopic(message) {
  const text = normalizeText(message);

  if (/(contact|correo|email|linkedin|github|contratar|contacto)/.test(text)) return "contact";
  if (/(ingles|english|idioma|language|b2)/.test(text)) return "language";
  if (/(backend|node|express|api|apis|docker|pm2|deploy|render)/.test(text)) return "backend";
  if (/(ia|ai|codex|chatgpt|gemini|pdf|forex|trading|legislativo|legislative)/.test(text)) return "ai";
  if (/(proyecto|project|portafolio|ecommerce|moodle|bridge)/.test(text)) return "projects";
  if (/(habilidad|skill|stack|tecnologia|technology|mern|mean|angular|mongo)/.test(text)) return "skills";
  if (/(experiencia|experience|trabajo|real|sdk|identy)/.test(text)) return "experience";

  return null;
}

function answerChat(message, forcedTopic) {
  const lang = getCurrentLanguage();
  const copy = chatbotCopy[lang];
  const topic = forcedTopic || detectTopic(message);
  const answer = topic && copy.topics[topic] ? copy.topics[topic].answer : copy.fallback;
  addChatMessage(answer, "bot");
}

function openChatbot() {
  chatbotPanel.hidden = false;
  chatbotToggle.setAttribute("aria-expanded", "true");

  if (!chatbotStarted) {
    addChatMessage(chatbotCopy[getCurrentLanguage()].welcome, "bot");
    chatbotStarted = true;
  }

  chatbotInput.focus();
}

function closeChatbot() {
  chatbotPanel.hidden = true;
  chatbotToggle.setAttribute("aria-expanded", "false");
}

chatbotToggle.addEventListener("click", () => {
  if (chatbotPanel.hidden) {
    openChatbot();
  } else {
    closeChatbot();
  }
});

chatbotClose.addEventListener("click", closeChatbot);

quickQuestionButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const topic = button.getAttribute("data-chat-topic");
    const copy = chatbotCopy[getCurrentLanguage()];
    const question = copy.topics[topic].question;
    addChatMessage(question, "user");
    answerChat(question, topic);
  });
});

chatbotForm.addEventListener("submit", (event) => {
  event.preventDefault();
  const message = chatbotInput.value.trim();

  if (!message) {
    return;
  }

  addChatMessage(message, "user");
  chatbotInput.value = "";
  answerChat(message);
});

function revealBottomMessage() {
  const scrollPosition = window.innerHeight + window.scrollY;
  const revealPoint = document.documentElement.scrollHeight - 180;
  bottomMessage.classList.toggle("visible", scrollPosition >= revealPoint);
}

window.addEventListener("scroll", revealBottomMessage, { passive: true });
window.addEventListener("resize", revealBottomMessage);
revealBottomMessage();
